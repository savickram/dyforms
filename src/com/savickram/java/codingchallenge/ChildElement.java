package com.savickram.java.codingchallenge;

public class ChildElement {

	public int childElementId;

	public String childElementName;

	public String childElementValueType;

	public String parentElementTriggerValue;

	public int getChildElementId() {
		return childElementId;
	}

	public void setChildElementId(int childElementId) {
		this.childElementId = childElementId;
	}

	public String getChildElementName() {
		return childElementName;
	}

	public void setChildElementName(String childElementName) {
		this.childElementName = childElementName;
	}

	public String getChildElementValueType() {
		return childElementValueType;
	}

	public void setChildElementValueType(String childElementValueType) {
		this.childElementValueType = childElementValueType;
	}

	public String getParentElementTriggerValue() {
		return parentElementTriggerValue;
	}

	public void setParentElementTriggerValue(String parentElementTriggerValue) {
		this.parentElementTriggerValue = parentElementTriggerValue;
	}
}
